<h1>About Six Degrees of FIRST</h1>
<div style="text-align:left;margin-left:auto;margin-right:auto;max-width:800px;"><p>Six Degrees of FIRST is a tool to find the shortest path between two FIRST teams. Two teams are considered linked together if they have shared an alliance in a past match. Then, the number of links between the two teams are calculated are the final list is output.</p>
<p>Six Degrees of FIRST is based on the websites <a href="http://oracleofbacon.org">The Oracle of Bacon</a> and <a href="http://en.wikipedia.org/wiki/Wikipedia:Six_degrees_of_Wikipedia">Six Degrees of Wikipedia</a>, which are based upon the the idea of <a href="http://en.wikipedia.org/wiki/Six_degrees_of_separation">Six Degrees of Seperation</a>, created by Frigyes Karinthy. Six Degrees of FIRST uses a <a href="http://en.wikipedia.org/wiki/Breadth-first_search">breadth-first search</a> to explore all possible paths, one step at a time, until a shortest one is found. We search our database (derived from The Blue Alliance's) for past alliances to find the shortest path possible between the two teams. Much thanks to <a href="http://www.thebluealliance.net">The Blue Alliance</a> for use of their match database. </p>
